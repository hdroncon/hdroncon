              <h5><b>Estratégias</b></h5>

              <p>Para atingir os indicadores de participação, <b>recomenda-se a contratação de 2 <i>Sales Representatives</i></b>: um fluente em Alemão e outro fluente em Espanhol e Português.
              <ul>
                <li>O funcionário fluente em Alemão poderá ficar dedicado ao comércio da Alemanha, que é muito forte, e alavancar as vendas na Áustria e na Suíça – as quais tem aumentado o consumo de produtos da Northwind. Por consequência, um funcionário ficaria livre da demanda destes países e poderia focar na prospecção de mais clientes em outros países (visando a meta de participação na UE), ou na manutenção dos já existentes.</li>
                <li>O funcionário fluente em Espanhol e Português é fundamental. O comércio no Brasil se apresenta sólido, de alto potencial, e é imprescindível que a devida atenção seja dada aos clientes brasileiros - para manutenção destes e prospecção de novos. Como <i>plus</i>, é possível explorar o mercado de Portugal, ainda tímido nos gráficos de faturamento da Northwind. Com a fluência em Espanhol, o atendimento aos clientes da Espanha e da América Latina também seria melhor conduzido, uma vez que já são três os países da América (Venezuela, Argentina e México) que tem como língua nativa o Espanhol consumindo produtos da Northwind. Não só isso: são vários outros países no continente que falam o Espanhol, assim, a prospecção de novos clientes em novos países também pode vir a ser facilitada.</li>
              </ul></p>

              <p>Visando as metas de faturamento e ticket médio, além da expansão de mercado através dos novos representantes de vendas, sugere-se investir em ações de <i><b>cross-selling</b></i> de produtos, que consiste no oferecimento de um item a princípio não considerado pelo ciente no momento da compra. Dessa forma, dentro de uma mesma compra, o faturamento é potencializado e o ticket médio, como consequência, sobe.</p>

              <p>Dentre tantas configurações de <i><b>cross-selling</b></i> possíves com os produtos* da Northwind Traders, propõe-se:
              <p>
                *Não foi levada em consideração aqui a coluna <i>discontinued</i> da tabela <i>products</i>.
              </p>
              <ul>
                <li>para os produtos <b>Camembert Pierrot</b>, <b>Raclette Courdavault</b> e <b>Gorgonzola Telino</b> (os três produtos mais vendidos na história da Northwind em quantidade, os três do tipo <i>Dairy</i>, que tende a harmonizar bem com bebidas, massas e chocolates), na etapa de finalização da compra, que sejam oferecidas bebidas (<i>Beverages</i>), doces (<i>Produce</i>) e massas (<i>Grains/Cereals</i>) ao cliente. No momento, é possível oferecer, por exemplo, <b>Sasquatch Ale</b> (<i>Beverage</i>, com preço baixo, de $14, e estoque elevado, com 111 unidades), <b>NuNuCa Nuss-Nogaut-Creme</b> (do tipo <i>Confections</i>, também com valor de $14 e estoque alto, em 76 unidades) e <b>Ravioli Angelo</b>, do grupo de <i>Grains/Cereals</i>. O Ravioli tem boa quantidade em estoque (36 unidades) e vem apresentando boas vendas no ano, tendo 35% das ordens de compra registradas para países falantes de Português ou Espanhol e 30% das ordens registradas para países onde é falado Alemão. Uma oportunidade secundária aqui é a <b>criação de uma campanha para alavancar a venda</b> deste pacote de produtos pelos dois novos funcionários que serão contratados para atender a demanda dos clientes falantes destes idiomas.</li>
                <li>que sejam criadas <b>cestas de produtos exclusivas</b>, contendo um item de cada categoria. É possível, por exemplo, criar uma cesta de produtos completa contendo <b>Côte-de-Blaye</b> (<i>Beverages</i>, $2635 por unidade), <b>Thüringer Rostbratwurst</b> (<i>Meat</i>, $12379 por unidade), <b>Raclette Cordavault</b> (<i>Dairy</i>, $55 por unidade), <b>Tarte au Sucre</b> (<i>Confections</i>, $493 por unidade), <b>Gnocchi di Nonna Alice</b> (<i>Grains/Cereals</i>, $38 por unidade) e <b>Manjumup Dried Apples</b> (<i>Produce</i>, $53 por unidade), que são 7 dos 8 produtos que mais geraram faturamento até agora para a Northwind Treaders e que possuem alto valor agregado, potencializando o aumento do ticket médio.</li>
                <li>que sejam feitos <b>pacotes de produtos</b>, como as cestas exclusivas citadas acima, mas com o objetivo de <b>diminuir o estoque</b> ou <b>alavancar produtos com pouca saída</b>. O item <b>Chocolade</b> (<i>Confections</i>), apesar de relativamente caro ($1275), teve 130 itens vendidos em 1997 e somente 8 em 1998, logo, poderia ser um item a ser considerado nas cestas exclusivas, uma vez que há estoque dele (15). Um produto do tipo <i>Confections</i>, ligeiramente mais acessível para a cesta e que apresenta comportamento semelhante é o <b>Schoggi Schokolade</b>, com valor de $439 e bom estoque disponível (49). Outro produto que pode vir a ser considerado para uma cesta é o <b>Röd Kaviar</b> (<i>Seafood</i>, $15 por unidade) que apresentou boas vendas em 1997 (256 itens) mas que não tem desempenhado tão bem em 1998, com apenas 12 itens vendidos e o agravante de ter 101 peças em estoque.</li>
              </ul></p>
