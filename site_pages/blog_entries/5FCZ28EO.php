              <h4><b>Negócios e metas</b></h4>
              <p>
                A análise de negócios - hoje muito difundida, inclusive no Brasil, como Business Intelligence - tem como principal meta entender, com o auxílio dos software de BI, o passado e presente das empresas através dos dados que ela gera. Ainda que não seja essa função primária de tais software, é por meio das análises históricas que é possível identificar falhas ou gargalos no negócio, motivos de queda de faturamento, compreensão e tendências de mercado, entre tantos outros fatores que, se melhorados, podem vir a trazer no futuro resultados positivos para a empresa.
              </p>
            </div>

            <div id="s03" class="w3-container w3-justify">
              <h4><b>Northwind Traders</b></h4>
              <p>
                A fim de oferecer uma amostra de banco de dados para teste aos seus usuários, a Microsoft disponibiliza no aplicativo Access os dados da empresa fictícia Northwind Traders. Neste exemplo, tem-se informações dos produtos comercializados pela Northwind, dados referentes as ordens de compra, estoque, fornecedores, clientes, colaboradores.. um banco de dados compacto, mas com insumos suficientes para uma primeira abordagem em análise de negócios. Neste projeto, utilizo o PowerBI para trazer os dados deste banco de dados, defino indicadores que acredito serem relevantes para a empresa e os analiso, sugerindo, através deles, oportunidades de melhoria e potenciais de faturamento.
              </p>

              <h5><b>Panorama Atual</b></h5>
              <p>
                Nota: a data da última entrada no arquivo <i>orders</i> foi a considerada como a data fictícia de criação deste projeto, a qual é 06 de maio de 1998.
              </p>

              <p>Até o presente momento, <i>06 de maio de 1998</i>, temos registrados 270 pedidos de compra no ano, com faturamento de $440.623,87. Tais vendas aconteceram majoritariamente na Europa (em 15 países diferentes) e nos Estados Unidos, no entanto, houveram vendas consideráveis na América do Sul, como por exemplo no Brasil, representando 10,4% do total das ordens de compra e 10,2% do faturamento do ano – ficando em quarto lugar neste ranking, atrás apenas de Estados Unidos, Alemanha e Áustria. O ticket médio atual é de $1.631,94. É importante ressaltar também que a equipe de vendas está distribuída entre os Estados Unidos e Reino Unido.</p>

