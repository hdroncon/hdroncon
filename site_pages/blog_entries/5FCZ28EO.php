              <h5><b>Análise histórica</b></h5>

              <p>Levando em consideração todos os dados disponíveis, é possível analisar informações sobre vendas que datam desde julho de 1996. Com estes recursos, observamos que, entre os anos de 1996 e 1997, houve aumento de aproximadamente 168% nos números de ordens de compra (de 152 para 408) e o faturamento foi de $208.083,97 para $617.085,20 - um aumento de aproximadamente 196%. O ticket médio cresceu 10,5%, saindo de $1.368,97 para $1.512,46.</p>

