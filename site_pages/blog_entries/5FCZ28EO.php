              <h5><b>Projeções futuras</b></h5>

              <p>Os números da Northwind Traders são sólidos e apresentam boas taxas de crescimento e, atrelados as análises até então apresentadas, é perceptível o espaço para melhora nos resultados da empresa.</p>

              <p>A fim de estipularmos metas e estratégias de mercado, estimaremos os dados do primeiro semestre de 1996 para melhor embasamento. Como os primeiros semestres de 1997 e 1998 são discrepantes – o primeiro apresenta aumento de vendas e faturamento, enquanto o outro aponta queda em ambos – não é possível identificar uma tendência do mercado neste período sem informações complementares. Assim, <b>consideraremos hipoteticamente</b> que o número de vendas/faturamento do primeiro semestre de 1996 é igual a 65% do performado no segundo semestre. Dessa forma, temos que o total de <b>vendas estimado</b> em 1996 é de 250, o <b>faturamento estimado</b> é de $343.338,55 e, consequentemente, o <b>ticket médio estimado</b> é de $1.373,35. Em relação a 1997, teríamos então um crescimento “<i>real</i>” de 63% em ordens de compra (250 para 408) e de 80% no faturamento ($343.338,55 para $617.085,20).</p>

