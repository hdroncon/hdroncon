              <h5><b>Metas</b></h5>

              <p>Com as considerações referentes a 1996 feitas e um parâmetro base de crescimento entre 1996 e 1997 estabelecido, recomenda-se que, para 1998, seja estipulado como meta o atingimento de <b>694</b> ordens de compra. Isto significaria um <b>aumento de 70%</b> em relação 1997 – superior, mas próximo dos 63% de aumento calculado entre os anos de 1996 e 1997. Da mesma forma, para o faturamento, recomenda-se a adoção do valor de <b>$1.141.607,62</b> como meta, representando <b>aumento de 85%</b> em relação a 1997 (o aumento calculado entre 1996 e 1997 foi de 80%). Com estes números, o ticket médio projetado para 1998 é de <b>$1644,97</b>, o que significa <b>aumento de 8,8%</b> deste em relação a 1997 (o aumento estimado do ticket médio entre 1996 e 1997 foi de 10%).</p>

              <p>Para uma melhor estimativa e definição das metas é imprescindível mais dados e informações. Mesmo assim, entende-se que os valores anteriormente recomendados são seguros e condizentes com o porte da Northwind Traders, pois estão próximos dos valores praticados nos anos anteriores e não deixam de visar o crescimento arrojado da empresa.</p>

              <p>Além dos indicadores econômicos, recomenda-se também aumentar a participação no mercado Europeu, buscando o comércio com no mínimo <b>mais 5 países</b> no continente (totalizando comércio com 20 países na União Européia). Nas Américas também é recomendado aumentar a participação expandindo o mercado com ao menos <b>mais 1 país</b>, de forma que a empresa esteja inserida em 6 países diferentes.</p>

