              <h4><b>Considerações</b></h4>
              <p>É possível analisar os dados disponíveis da Northwind Traders de muitas formas e métodos diferentes. Aqui, decidi não aprofundar em métodos estatísticos ou computacionais avançados - vulgo preditivos - pois, no meu entendimento, sequer há um histórico sólido de informações (no caso, tem-se dados de um ano e meio de operação) que de fato permita resultados conclusivos aplicando ferramentas de alta complexidade. Ainda que o período seja curto, são várias tabelas com um volume considerável de dados, que também não cheguei a explorar por completo. Fica claro então que o espaço para melhorias e expansão dos resultados que apresentei é enorme!</p>
