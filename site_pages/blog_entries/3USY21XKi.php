            <div class="w3-container w3-justify">
              <h4><b>The encounter between soccer and videogames</b></h4>
              <p>People interested in soccer and video games - and even those who don't fit into these worlds - have probably heard of the game FIFA (which, after many editions, is coming with a new name in the next season). It features various game modes where you can simulate real or user-created football matches with real teams and players. I believe the most interesting, controversial, and spotlighted mode in the community is Ultimate Team, where the user creates a team, signs players, and competes against other users online with the goal of acquiring more resources, improving the team, facing off against other online users... and keeping the cycle going!</p>
            </div>

            <div id="s02" class="w3-container w3-justify">
              <h4><b>Business intelligence</b></h4>
              <p>Business intelligence is a field of immense complexity and proportions, with one of its key pillars being data analysis. This enables businesses and marketing teams, for example, to make decisions aimed at predefined objectives, such as increasing sales or determining the optimal location for opening a new branch. One of the software tools that assist in this activity is PowerBI (Business Intelligence), which aims to present data and insights in a friendly and clear manner, facilitating decision-making for teams analyzing the dashboards.</p>
            </div>

            <div id="s03" class="w3-container w3-justify">
              <h4><b>Scouting department</b></h4>
              <p>In soccer, scouting refers to the role of analyzing the performance of teams and players, with one of the objectives being to identify and monitor professionals with exceptional potential who may be considered for recruitment by a particular team. Information related to goals scored, injury history, number of cards received, age, among many other variables, generates data that scouts analyze and verify to prepare reports that are presented to the management. This process of recruitment, in collaboration with the coaching staff, is then focused on specific names that align with the club's objectives.
              </p>
              <p>Such data, whether for the real world or the Ultimate Team universe, can be difficult or impossible to access due to confidentiality or the simple fact that they are not collected. However, it is possible to find fragments of data for free on the internet, enabling studies like this one where I used Ultimate Team data that I found on Kaggle to simulate scouting within a real football club through Power BI dashboards.
              </p>
              <p>The idea is very straightforward: I define the position I'm looking for a player, apply some filters, and a list of names is generated. For each of the players, I can then check additional information that allows me to determine if that individual should indeed be included in the recruitment analysis report for the coaching staff. Click on the images to enlarge them.
              </p>
              <div class="w3-center">
                <img src="blog_images_01-10/3USY21XK_01.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Scout dashboard</figcaption>
              </div>
              <p>In the <b>Field</b>, I click on the position I want to analyze (in this file I'm providing, only ST, LM, and CDM are available - the logic would be the same for other positions).</p>
              <div class="w3-center">
                <img src="blog_images_01-10/3USY21XK_02.png" style="width:20%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Field and positions</figcaption>
              </div>
              <p>In the <b>Wage</b> filter, I set the current salary that the player earns, allowing me to establish values within the budget set by the management.</p>
              <p>In the <b>Potential</b> filter, I define the minimum potential of the player. Here, I did a simple calculation: potential is determined by the inverse of the age plus the overall projected for the next season (this is a value ranging from 0 to 100 that FIFA establishes through its own criteria).</p>
              <p>I leave the <b>Overall</b> field open because I am considering both promising players (with a low overall) and already established players (with a high overall).</p>
              <div class="w3-center">
                <img src="blog_images_01-10/3USY21XK_03.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Filters</figcaption>
              </div>
              <p>Let's suppose, for example, that we are looking for a striker (ST) with a salary of up to 15,000 Euros and a potential between 1.25 and 1.3863. With these filters, one of the players listed is Mohamed Daramy. On the dashboard, you can see that he is 21 years old, 1.80 meters tall, and Danish. Furthermore, he is currently playing for Ajax in the Dutch Eredivisie league, with a contract until 2026, a salary of 5,000 Euros, a market value of 3 million Euros, and a contract release clause set at 7 million Euros.</p>
              <div class="w3-center">
                <img src="blog_images_01-10/3USY21XK_04.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>General view</figcaption>
              </div>
              <p>Daramy's <b>potential</b> is 1.2962. In the graph, it shows the highest potential value considering the entire database (1.39) and the <i>setpoint</i> of 1.1815 – which I established as 85% of the maximum possible potential. Therefore, players with values above the setpoint have growth and performance characteristics well above average.</p>
              <p>You can also see that the player's overall rating is relatively similar among the positions of ST, CF, RF, LF, and CAM (<b>Stats per Position</b>), indicating that he is versatile and can be utilized in positions other than ST.</p>
              <p>There have been data for Daramy in the FIFA database since 2020. In 2020, he had an overall rating of 57, in 2021 it was 67, in 2022 it reached 69, and the projection is that in the 2023 season, it will reach 82. These numbers show consistent and continuous improvement.</p>
              <div class="w3-center">
                <img src="blog_images_01-10/3USY21XK_05.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Overall through the years</figcaption>
              </div>
              <p>Indeed, considering all the information on the dashboard, Mohamed Daramy appears to be a compelling prospect for evaluation. His attributes are well-balanced, and he possesses qualities such as speed (around 90 PAC/pace), good dribbling (approximately 60 DRI), a strong shot (~60 SHO), good physical attributes (around 65 PHY), and solid passing ability (around 60 PAS). Additionally, his lower defensive rating (~15 DEF) is not a concern, as he is primarily an attacker. These attributes make him a promising candidate for consideration.</p>
              <div class="w3-center">
                <img src="blog_images_01-10/3USY21XK_06.png" style="width:30%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Attributes</figcaption>
              </div>
              <p>Now, let's say we're looking for an excellent defensive midfielder (CDM). Salary and market value are not a concern; we are solely interested in a player with growth potential within the range set by the management (1.1815) and an overall rating above 85.</p>
              <p>Among the listed names, Dutch player Frenkie de Jong stands out. His overall rating has been consistently increasing each year, he is young, and he transitions well between midfield positions. He possesses high attributes for both defensive and creative aspects of the game, such as physicality and passing ability. However, there is a potential obstacle: his contract with Barcelona extends until 2026, and his release clause is set at 254 million Euros. Depending on the available budget for the new CDM, this could be a limiting factor for his potential recruitment.</p>
              <div class="w3-center">
                <img src="blog_images_01-10/3USY21XK_07.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>A very promising CDM.</figcaption>
              </div>
            </div>


            <div id="s04" class="w3-container">
              <h4><b>Consideration</b></h4>
              <p>PowerBI allows for relatively in-depth data analysis, justifying the term Business Intelligence; however, I didn't delve into those tools here. The focus was on data manipulation and table creation, and primarily on developing routines in DAX, which is the language used by the software.
              </p>
              <p>One feature I felt was missing in this development was player comparison. Assuming we have a limited list of names, it would be interesting to automate a method to compare them within the dashboard itself. This is a future implementation for sure.</p>
              <p>A technical detail to note is that PowerBI does not have a native radar chart visualization. So, the only free way to plot attributes was through <b>Python</b> visuals. You need to have a Python compiler set up for the chart to be plotted. If you encounter visualization issues, I recommend checking the path: File/Options and Settings/Options/Python Scripting, where instructions for installing Python are provided. Additionally, in the code, I use the libraries <b>matplotlib.pyplot</b>, <b>numpy</b> e <b>pandas</b>, which need to be installed as well (I recommend using pip for installation).</p>
            </div>

