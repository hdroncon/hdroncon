            <div class="w3-container w3-justify">
              <h4><b>O encontro entre futebol e videogame</b></h4>
              <p>Pessoas que se interessam por futebol e videogame - e até quem não se encaixa nestes universos - provavelmente já ouviram falar no jogo FIFA (o qual, depois de muitas edições, está chegando com um novo nome na próxima temporada). Nele estão presentes diversos modos de jogo onde é possível simular partidas de futebol com times e jogadores reais ou criados pelos usuários. Acredito que o modo mais interessante, polêmico e que recebe mais holofotes da comunidade é o <b>Ultimate Team</b>, onde o usuário cria um time, contrata jogadores e disputa contra outros usuários online, com o objetivo de conquistar mais recursos, melhorar o time, disputar contra outros usuários online.. e manter o ciclo! </p>
            </div>

            <div id="s02" class="w3-container w3-justify">
              <h4><b>Inteligência de mercado</b></h4>
              <p>A inteligência de mercado é um campo de complexidade e proporções absurdas que tem como um dos principais pilares a análise de dados, que permite, por exemplo, guiar negócios, times de marketing, a tomar decisões buscando um objetivo pré-definido, como o aumento de vendas ou a definição do local ótimo de abertura de uma nova filial. Um dos software que auxiliam nesta atividade é o PowerBI (de <i>business inteligence</i>), que visa mostrar dados, insights, de forma amistosa e clara, a fim de facilitar as tomadas de decisão pelas equipes que analisam os dashboards.</p>
            </div>

            <div id="s03" class="w3-container w3-justify">
              <h4><b>Departamento de Scouting</b></h4>
              <p>Scouting, no futebol, refere-se a função de analisar o desempenho de equipes e jogadores, tendo como um dos objetivos identificar e monitorar profissionais com potencial diferenciado para serem, talvez, contratados para um determinado time. Informações a respeito de gols marcados, histórico de lesões, número de cartões, idade, entre tantas outras variáveis geram dados que os scouts analisam e verificam para prepararem relatórios que são entregues à diretoria de forma que o processo de contratação em conjunto com a comissão técnica seja focado em nomes específicos, já alinhados com os objetivos do clube.
              </p>
              <p>Tais dados, tanto para o mundo real quanto para o universo do Ultimate Team, são de difícil ou impossível acesso, seja por sigilo ou pelo simples fato de não serem coletados. No entanto, é possível encontrar <i>pedaços</i> de dados gratuitamente na internet, que possibilitam estudos como este aqui, onde utilizei dados do Ultimate Team que encontrei no Kaggle para simular o scouting dentro de um clube de futebol real através de dashboards em Power BI.
              </p>
              <p>A ideia é muito simples: defino a posição que estou buscando um jogador e, com alguns filtros, uma lista de nomes é gerada, onde posso, para cada um dos jogadores, verificar informações complementares que me permitem verificar se de fato aquele indivíduo pode ser incluído no relatório de análise de contratação da comissão técnica. Clique nas imagens para aumentá-las.
              </p>
              <div class="w3-center">
                <img src="blog_images_01-10/3USY21XK_01.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Dashboard Scout</figcaption>
              </div>
              <p>No <b>Campo</b>, clico na posição que desejo analisar (neste arquivo que disponibilizo, estão disponíveis apenas ST, LM e CDM - a lógica seria a mesma para as demais posições).</p>
              <div class="w3-center">
                <img src="blog_images_01-10/3USY21XK_02.png" style="width:20%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Campo e posições</figcaption>
              </div>
              <p>No filtro <b>Wage</b> defino o salário atual que jogador ganha, assim posso estabelecer valores dentro do orçamento da diretoria.</p>
              <p>No filtro <b>Potential</b> defino qual é o potencial mínimo do jogador. Aqui, fiz uma conta simples: o potencial é dado pelo inverso da idade mais o <i>overall</i> previsto para a próxima temporada (este é um valor de 0 a 100 que o FIFA estabelece através de critérios próprios).</p>
              <p>O campo <b>Overall</b> deixo em aberto pois estou considerando promessas (de <i>overall</i> baixo) e jogadores já consolidados (<i>overall</i> alto).</p>
              <div class="w3-center">
                <img src="blog_images_01-10/3USY21XK_03.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Filtros</figcaption>
              </div>
              <p>Suponhamos, por exemplo, que estamos buscando um atacante (ST), com salário de até 15 mil Euros e com potencial entre 1,25 e 1,3863. Com estes filtros, um dos jogadores listados é o Mohamed Daramy. No dashboard, é possível verificar que ele tem 21 anos, 1,80m de altura e é dinamarquês. Além disso, ele está atualmente no Ajax da liga holandesa (Eredivisie), com contrato até 2026, salário de 5 mil Euros, valor de mercado de 3 milhões de Euros e cláusula de quebra de contrato fixada em 7 milhões de Euros.</p>
              <div class="w3-center">
                <img src="blog_images_01-10/3USY21XK_04.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Panorama geral</figcaption>
              </div>
              <p>O <b>potencial</b> do Daramy é de 1,2962. No gráfico, é mostrado o maior valor de potencial considerando toda a database (1,39) e o <i>setpoint</i> de 1,1815 – este eu estabeleci como 85% do potencial máximo possível. Assim, jogadores que apresentam valor superior o setpoint tem características de crescimento e performance muito acima da média.</p>
              <p>É possível ver também que o jogador apresenta overall relativamente parecido entre as posições de ST, CF, RF, LF e CAM (<b>Stats per Position</b>), o que mostra que ele é versátil e pode ser aproveitado fora da posição de ST.</p>
              <p>Há dados do Daramy desde 2020 na database do FIFA. Em 2020 ele tinha overall de 57, em 2021 era 67, em 2022 foi 69 e a previsão é de, na temporada de 2023, atingir 82. Os números são crescentes e sólidos.</p>
              <div class="w3-center">
                <img src="blog_images_01-10/3USY21XK_05.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Overall através dos anos</figcaption>
              </div>
              <p>Os atributos dele também são interessantes e balanceados. Ele é rápido (~90 de PAC/velocidade), apresenta bom drible (~60 de DRI), bom chute (~60 de SHO), bom físico (~65 de PHY), bom passe (~60 de PAS) e baixa defesa (~15 de DEF, o que não é um problema, pois é um atacante). Considerando todas as informações do dashboard, um nome interessante a ser avaliado!</p>
              <div class="w3-center">
                <img src="blog_images_01-10/3USY21XK_06.png" style="width:30%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Atributos</figcaption>
              </div>
              <p>Agora suponhamos que queremos um excelente volante (CDM). Não me interessa o seu salário ou valor de mercado, apenas que apresente potencial de crescimento dentro do estabelecido pela diretoria (1,1815) e overall acima de 85.</p>
              <p>Dentre os nomes listados, o holandês Frenkie de Jong se destaca. O overall dele vem crescendo a cada ano, é jovem, transita bem entre posições no meio de campo e tem atributos altos para um jogador de marcação e criação, como o físico e passe. O problema é que seu contrato com o Barcelona vai até 2026 e sua multa contratual é de 254 milhões de Euros, o que pode ser um entrave dependendo do montante disponível para contratação do novo CDM do time.</p>
              <div class="w3-center">
                <img src="blog_images_01-10/3USY21XK_07.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Um potencial CDM para contratação</figcaption>
              </div>
