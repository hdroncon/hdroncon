            <div id="s04" class="w3-container">
              <h4><b>Considerações</b></h4>
              <p>O PowerBI permite análises relativamente profundas de dados, justificando o termo Business IntelLigence, no entanto, não explorei tais ferramentas aqui. O foco foi na manipulação e criação de tabelas e, principalmente, no desenvolvimento de rotinas em DAX, que é a linguagem utilizada pelo software.
              </p>
              <p>Um recurso que senti falta nesse desenvolvimento foi o de comparação entre jogadores. Supondo que temos uma lista restrita de nomes, seria interessante automatizar um método para compará-los entre si no próprio dashboard. Implementação futura, com certeza.</p>
              <p>Um detalhe técnico é de que o PowerBI não apresenta visual nativo de gráfico do tipo radar, então, a única forma gratuita de fazer a plotagem de atributos foi através do visual <b>Python</b>. É preciso ter um compilador de Python para que o gráfico seja plotado, logo, caso esteja com problemas de visualização, sugiro verificar o caminho Arquivo/Opções e Configurações/Opções/Python Scripting, onde são dadas instruções de instalação do Python. Além disso, no código, uso as bibliotecas <b>matplotlib.pyplot</b>, <b>numpy</b> e <b>pandas</b>, as quais é preciso que sejam instaladas também (recomendo pelo pip).</p>
