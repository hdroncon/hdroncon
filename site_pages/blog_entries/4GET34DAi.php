            <div id="s04" class="w3-container">
              <h4><b>Considerações</b></h4>
              <p>
                I'm still getting the hang of this blog's dynamics. In this post, I tried to explain the project in a general way, its purpose, and its application. In a parallel post, I delve into the technical aspects of what was done, the code, the logic, which will help me document this project and may serve as a source of information for my future work or for others as well. <a style="color:red; font-weight: bold;" href="#s04" style="text-decoration:none">This post can be read here...</a>
              </p>
              <p>
                One of the main points of this application is that, among the inherent <b>CRUD</b> (Create, Read, Update, Delete) operations in databases, it only covers Create and Read. It's not possible to update or delete quotes. Since the database handling is a secondary factor in this project, I consider myself satisfied with this point.
              </p>
              <p>
                Another focus here was on the automatic creation of a PDF file containing the database information. The library I decided to explore for this was <b>FPDF</b>, which has clear documentation and straightforward usage.
              </p>
              <p>Some points for future improvements:
              <ol>
                <li>add a field to display the percentage chosen for the darkness level of the window tint.</li>
                <li>limit the number of characters in the Notes, Name, Email, and Phone fields and make them mandatory.</li>
                <li>allow only numbers in the Phone field and check if the Email is valid (by checking if the field contains "@" or ends with ".com" or ".com.br," for example).</li>
                <li>a warning window if the quote was generated or if there was an error.</li>
              </ol> 
              </p>
