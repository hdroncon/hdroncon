              <h4><b>Efficient Applications and Solutions</b></h4>
              <p>
                Developing robust applications is not currently part of my day-to-day activities, and it's not a subject I'm well-versed in, so I can't go into great detail as the risk of making mistakes is high. However, it's an area I have a lot of interest in, and I try to absorb knowledge from articles I read whenever possible.
              </p>
              <p>
                Even though I lack deep technical knowledge of the terminology, applications, and the true scope of the field, it's a fact that I enjoy applications, automation, problem-solving, and programming. Regardless of how simple and quick a task may be, if I understand that it's possible to automate that activity - whether to make the process even faster, standardize it, or simply reduce/eliminate the need for user interaction to prevent data entry errors - I believe it's worth dedicating time to automate it or, at the very least, create methods that facilitate user input.
              </p>
            </div>

            <div id="s02" class="w3-container w3-justify">
              <h4><b>Defining Paths</b></h4>
              <p>
                The development of graphical user interfaces (<b>GUI</b>) has been an area of interest for me for some time. Creating calculation routines and methods has been relatively straightforward and familiar tasks, but making the use of these resources user-friendly and independent of minimal knowledge of a particular software has always been a challenge. The initial steps were taken with <b>Excel/VBA</b>, which works reasonably well in this scope but has limitations due to the simple fact that it is not software designed for this purpose. Since I have some knowledge of <b>Python</b>, I explored the <b>Tkinter</b> library, which is intended for GUI development and serves its purpose well, but I was not entirely satisfied with its visual limitations. In this same vein, we have the excellent <b>PyQT</b> and <b>PySide</b> libraries, although I didn't venture into them at the time due to not fully understanding the legal means of distributing applications developed with them. Nowadays, these legal issues seem clearer to me, and I intend to use them in the near future.
              </p>
              <p>
                In this project, I focused on the intersection of <b>CSS</b>, <b>HTML</b>, <b>Javascript</b>, and <b>Python</b> through the <b>Eel</b> library. While my knowledge of <i>front-end</i> technologies is quite limited, it's not (so) difficult to find resources on the internet for developing responsive websites. Moreover, the visual presentation that these tools offer is very appealing. Using Eel was a great choice to test (and improve) my skills in these web development tools, combined with Python, which I'm also quite comfortable with.
              </p>
            </div>

            <div id="s03" class="w3-container w3-justify">
              <h4><b>Budgeting App</b></h4>
              <p>
                In this application, I developed a user-friendly graphical interface for creating quotes - in this case, for vehicle sales. The process, which offers a high degree of customization in terms of option selection, becomes standardized and makes it easy to navigate through the stages of defining the package. Furthermore, the automatic generation of the quote itself eliminates the need for any calculations on the part of the estimator.
              </p>
              <p>
                In the main column, you first need to choose the vehicle <b>Model</b> from the four available options.  
              </p>

              <div class="w3-center">
                <img src="blog_images_01-10/4GET34DA_01.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Available models.</figcaption>
              </div>

              <p>
                The <b>Accessories</b> are also four - <b>Steering Assistant</b>, <b>Braking Assistant</b>, <b>Automatic Transmission</b>, and <b>Hybrid Mode</b>. These can be combined in any way, as the selection boxes are independent. 
              </p>

              <div class="w3-center">
                <img src="blog_images_01-10/4GET34DA_02.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Acessories.</figcaption>
              </div>

              <p>
                In order to implement more responsive features available in HTML, I created a second section for additional options. Here, you can choose the <b>level of darkness</b> for the window film, the <b>radio model</b>, and whether you want to test the vehicle on a racetrack (<b>track day</b>). For the radio, the default option is '<b>Padrão/Default</b>', and to change it, you need to check the respective selection box. If you choose a different model from the default and then uncheck the box, the model automatically reverts to the default option.
              </p>

              <div class="w3-center">
                <img src="blog_images_01-10/4GET34DA_03.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Complementary acessories.</figcaption>
              </div>

              <p>
                Similar to the vehicle model, in the <b>Packages</b> section, you can choose only one of the services offered in the application.
              </p>

              <div class="w3-center">
                <img src="blog_images_01-10/4GET34DA_04.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Packages.</figcaption>
              </div>

              <p>
                Finally, <b>Customer Information</b> is required. There is an <b>Observations</b> field where the customer can write relevant notes about the request. Additionally, the <b>Name</b>, <b>Email</b>, and <b>Phone Number</b> are also requested so that the quote can be specifically directed to this customer, and they can be contacted in the future if necessary. After reviewing all the information, simply press the <b>Generate Invoice</b> button to create the document with the page's information.
              </p>

              <div class="w3-center">
                <img src="blog_images_01-10/4GET34DA_05.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Client information.</figcaption>
              </div>

              <p>
                The quotes are automatically saved in the <i>\web\invoice</i> directory with sequential nomenclature. At the top of the document, we have the header with general information and the document date.
              </p>

              <div class="w3-center">
                <img src="blog_images_01-10/4GET34DA_06.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Quote header.</figcaption>
