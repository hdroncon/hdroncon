              <h4><b>Definindo caminhos</b></h4>
              <p>A elaboração de interfaces gráficas (do inglês <i>graphical user interface</i>, <b>GUI</b>) tem sido um tópico de meu interesse já há algum tempo. Elaborar rotinas de cálculo, métodos, são tarefas relativamente tranquilas e familiares, no entanto, fazer com que a utilização destes recursos fosse amigável e independente de conhecimentos mínimos de determinado software sempre foi um problema. Os primeiros passos vieram com o <b>Excel/VBA</b>, que funciona relativamente bem neste escopo, mas apresenta limitações, pelo simples fato de que não é um software com esta finalidade. Como tenho algum conhecimento em <b>Python</b>, explorei a biblioteca <b>Tkinter</b>, voltada para a elaboração de GUI’s, a qual funciona bem dentro do seu propósito, mas que também não me agradou pelas suas limitações visuais. Ainda nesta linha, temos as excelentes <b>PyQT</b> e <b>PySide</b>, nas quais não cheguei a me aventurar por não entender os meios legais para distribuição de aplicativos desenvolvidos com elas – hoje tais questões me parecem mais claras e pretendo utilizá-las em breve.
              </p>
              <p>Neste projeto, foquei na junção entre <b>CSS</b>, <b>HTML</b>, <b>Javascript</b> e <b>Python</b> através da biblioteca <b>Eel</b>. Ainda que meus conhecimentos sobre <i>front-end</i> sejam bem limitados, não é (tão) difícil buscar recursos na internet para o desenvolvimento de sites responsivos, além disso, a apresentação visual que estas ferramentas proporcionam é muito interessante. A opção de usar a Eel então caiu muito bem para testar (e aprimorar) meus conhecimentos nestas ferramentas de desenvolvimento web, atrelado ao próprio Python, pelo qual também tenho bastante afinidade.
              </p>
            </div>

            <div id="s03" class="w3-container w3-justify">
              <h4><b>Aplicativo de orçamentos</b></h4>
              <p>Neste aplicativo desenvolvi uma interface gráfica amigável para a elaboração de orçamentos – no caso, para a venda de veículos. O processo, que apresenta alta possibilidade de combinação nas opções de escolha, com o aplicativo, torna-se padronizado e facilita a navegação entre as etapas de definição do combo, além disso, a geração automática do orçamento em si também elimina qualquer necessidade de cálculo por parte do orçamentista.</p>
              <p>Na coluna principal, é necessário primeiro escolher o <b>Modelo</b> do veículo dentre as quatro opções disponíveis.</p>
              <div class="w3-center">
                <img src="blog_images_01-10/4GET34DA_01.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Modelos disponíveis.</figcaption>
              </div>
              <p>Os <b>Acessórios</b> também são quatro – <b>Assistente de Direção</b>, <b>Assistente de Frenagem</b>, <b>Câmbio Automático</b> e <b>Modo Híbrido</b>. Estes podem ser combinados de qualquer forma, uma vez que as caixas de seleção são independentes.</p>
              <div class="w3-center">
                <img src="blog_images_01-10/4GET34DA_02.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Acessórios.</figcaption>
              </div>
              <p>A fim de implementar mais recursos responsivos disponíveis no HTML, fiz uma segunda seção de adicionais. Aqui é possível escolher o <b>nível de escuridão da película dos vidros</b>, o <b>modelo de rádio</b> e se é desejado <b>testar o veículo em um autódromo</b> (<i>track day</i>). Para o rádio, a opção padrão é a <b>Padrão/Default</b> e, para mudá-la, é preciso primeiro checar a respectiva caixa de seleção. Caso seja escolhido um modelo diferente do padrão e a caixa seja desmarcada, o modelo volta automaticamente para a opção default.</p>
              <div class="w3-center">
                <img src="blog_images_01-10/4GET34DA_03.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Acessórios complementares.</figcaption>
              </div>
              <p>Semelhante ao modelo do veículo, na seção de <b>Pacotes</b> é possível escolher somente um dos serviços oferecidos no aplicativo.</p>
              <div class="w3-center">
                <img src="blog_images_01-10/4GET34DA_04.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Pacotes de recursos.</figcaption>
              </div>
              <p>Por fim, são requeridas <b>Informações do Cliente</b>. Há um campo de observações, onde o cliente pode escrever <b>Notas</b> relevantes sobre o pedido. Além disso, o <b>Nome</b>, <b>Email</b> e <b>Número de Telefone</b> também são solicitados, de forma que o orçamento seja direcionado de forma específica para este cliente e seja possível contatá-lo no futuro, se necessário. Após conferir todas as informações, basta pressionar o botão <b>Generate Invoice</b> para gerar o documento com as informações da página.</p>
              <div class="w3-center">
                <img src="blog_images_01-10/4GET34DA_05.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Infos gerais do cliente.</figcaption>
              </div>
              <p>Os orçamentos são salvos automaticamente no diretório <i>\web\invoice</i>, com nomenclatura sequencial. No topo do documento, temos o cabeçalho com informações gerais e a data do documento.</p>
              <div class="w3-center">
                <img src="blog_images_01-10/4GET34DA_06.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Cabeçalho do orçamento.</figcaption>
              </div>
              <p>Depois, são discretizados os itens do orçamento, bem como o valor atrelado a cada um dos acessórios escolhidos através do aplicativo.</p>
              <div class="w3-center">
                <img src="blog_images_01-10/4GET34DA_07.png" style="width:50%" onclick="onClick(this)">
                <figcaption>Discretização de valores do orçamento.</figcaption>
              </div>
              <p>Por fim, as observações inseridas na elaboração do orçamento são mostradas abaixo da tabela de valores do orçamento.</p>
              <div class="w3-center">
                <img src="blog_images_01-10/4GET34DA_08.png" style="width:50%" class ="image_hv" onclick="onClick(this)">
                <figcaption>Notas do orçamento.</figcaption>
