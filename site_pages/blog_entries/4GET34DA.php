              <h4><b>Aplicativos e soluções eficientes</b></h4>
              <p>O desenvolvimento de aplicativos robustos é um tópico que não faz parte do meu dia-a-dia atualmente e não se trata de um assunto que domino, assim, não posso me estender nas explicações pois o risco de me equivocar é alto. No entanto, é uma área que tenho bastante interesse e que, na medida do possível, tento absorver algo dos artigos que leio.</p>
              <p>Embora falte conhecimento técnico profundo dos termos, das aplicações e da real abrangência da área, é fato que eu gosto de aplicativos, de automação, de resolver problemas e de programação. Por mais simples e rápida que uma tarefa seja, se entendo que é possível automatizar esta atividade – seja para agilizar ainda mais o processo, torná-lo padronizado ou para simplesmente reduzir/eliminar a necessidade de interação do usuário para evitar erros de inserção de dados – já acredito que vale a pena desprender um tempo para torná-la automática, ou, ao menos, criar métodos que facilitem o input dos usuários.</p>
            </div>
