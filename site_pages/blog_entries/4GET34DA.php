              <h4><b>Considerações</b></h4>
              <p>Ainda estou estudando a dinâmica deste blog. Neste post tentei explicar de maneira geral o projeto, seu objetivo e aplicação e, em um post paralelo, entro na técnica do que foi feito, os códigos, a lógica, o que me ajudará na documentação deste projeto e pode servir de fonte de informação para meus futuros trabalhos ou para outras pessoas também. <a style="color:red; font-weight: bold;" href="#s04" style="text-decoration:none">Este post pode(rá) ser lido aqui.</a>.</p>
              <p>Um dos principais pontos deste aplicativo é que, dos recursos <b>CRUD</b> (traduzido do inglês: criar, ler, atualizar e deletar) inerentes em banco de dados, este contempla apenas o criar e ler, não sendo possível atualizar orçamentos ou deletá-los. Como o tratamento de banco de dados é fator secundário neste projeto, me considero satisfeito neste ponto.</p>
              <p>Outro foco aqui foi na criação automática de um arquivo em PDF que trouxesse as informações do banco de dados. A biblioteca que decidi explorar aqui foi a <b>FPDF</b>, de documentação bastante clara e de utilização bem direta.</p>
              <p>Alguns pontos para melhorias futuras: 
              <ol>
                <li>adicionar um campo de visualização de quantos % está sendo escolhido para o nível de escuridão da tinta do vidro.</li>
                <li>limitar a quantidade de caracteres nos campos de Notas, Nome, Email e Telefone e fazer com que tenham preenchimento obrigatório.</li>
                <li>permitir somente números no campo Telefone e verificar se o Email é válido (checando se o campo possui o “@” ou se termina com “.com” ou “.com.br”, por exemplo).</li>
                <li>uma janela de aviso se o orçamento foi gerado ou se houve algum erro.</li>
              </ol> 
              </p>
